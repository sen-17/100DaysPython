# Data types
# String
print("Hello" [0]) # printing the first letter

# Integer =  Whole Number
print(123)

# Large Numbers
print(123_456_789) # doesn't print the underscore

# Float = Decimals
print(6.8)

# Boolean = True or False
print(True)

# Check types using type function
print(type(123))

# Mathematical Operators
# Addition (+)
# Substraction (-)
# Division (/)
# Multiplication (*)
# Exponential (**)
# Floor (//)

print(round(30.555))